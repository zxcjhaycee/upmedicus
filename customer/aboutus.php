<h2>COLLEGE OF MEDICINE (F.B. Herrera Jr., Medical Library) </h2>

<p>
The F.B. Herrera Jr., Medical Library of UP Manila plays a pivotal role in the College of Medicine. It provides quality support to the instruction, research and extension programs of the college. Housed in a two-storey building, the library was named Dr. Florentino B. Herrera, Jr. on October 9, 1986 in honor of the former Dean of the College of Medicine and first Chancellor of UP Manila. The library has a sitting capacity of 148, a multimedia room, computer system, and CD-ROM players. It has an Electronic Medical Research and Library Database System (EMERALDS) with work stations and OVID Biomedical collection of 60 medical journals in full-text from 1993 to present and ProQuest, MDConsult. The reading rooms and stacks are airconditioned. </p>
<p>
The Library collection of 65,304 volumes covers the basic clinical sciences, the history of medicine, its specialties and the allied health sciences. The library resources consist of 16,665 books, 25,181 bound periodicals, 16,665 special collection, 7,367 audio-visual (AV) materials, 937 serial titles. Reference materials, both in print and electronic format and multi-media are available. </p>
<p>
The Philippine Index Medicus, the Union List of Medical Periodicals, theses, professorial chair lectures and local data bases are available on-line. The On-Line Public Access Catalog is linked to UP Manila's Web Page which accessible 24 hours a day using any web browser, It has internet and electronic mail facilities. </p>
<p>
The Medical Library is the National SEAMIC EDDS Center, a member of Nutrition Research Information Network (NUTRINET), and WHO/WPRO reference library of WHO publications. </p>
